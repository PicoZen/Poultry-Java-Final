    public void  autoID() {
        try {
            
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/poultry","root","root");
            St = con.createStatement();
            Rs = St.executeQuery("Select Max(Userid) from users where Userid like "+"'F%'");
            
            Rs.next();
            
            Rs.getString("Max(Userid)");
            
            if(Rs.getString("Max(Userid)")==null ) {
                FarmID.setText("F100");
            }
            
            else {
                Long id = Long.parseLong(Rs.getString("Max(Userid)").substring(1,Rs.getString("Max(Userid)").length()));
                id ++;
                FarmID.setText("F" + String.format("%03d",id));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FarmerReg.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FarmerReg.class.getName()).log(Level.SEVERE, null, ex);
        }
    }    
           
    
