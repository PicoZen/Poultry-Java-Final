    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String plac =place.getSelectedItem().toString() ;
        if(plac.equals("Select District")){
            JOptionPane.showMessageDialog(this, "Please Select Your District");
        
        }
        else {
        if(FarmID.getText().isEmpty() ||name.getText().isEmpty() || Pwd.getText().isEmpty() || place.getSelectedItem().toString().isEmpty() ||num.getText().isEmpty() || role.getText().isEmpty())
         {
              JOptionPane.showMessageDialog(this,"Missing Information");
         } 
        else{
            try {
            
            String iden = FarmID.getText();
            String nam = name.getText();
            String pwd = Pwd.getText();
            String pla = place.getSelectedItem().toString();
            String rol = role.getText();
            String no = num.getText();
          
            
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/poultry", "root", "root");
            PreparedStatement add = con.prepareStatement("insert into users values(?,?,?,?,?,?)"); 
            PreparedStatement sub = con.prepareStatement("insert into farmer values(?,?,?,?,?,?,?)");
            add.setString(1, iden);
            add.setString(2, nam);
            add.setString(3, pwd);
            add.setString(4, rol);
            add.setString(5, pla);
            add.setString(6, no);
            add.executeUpdate();
            
            sub.setString(1,iden);
            sub.setString(2,nam);
            sub.setString(3,"");
            sub.setString(4,"");
            sub.setString(5,pla);
            sub.setString(6,no);
            sub.setString(7,"");
            sub.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Registered successfully");
            JOptionPane.showMessageDialog(this, "User id : "+iden+ "\n Role : "+rol);
            con.close();
            
            new LoginPage().setVisible(true);
            this.dispose();
            
         
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(this, e);
           
        
        }
        }
        }
    }//GEN-LAST:event_jButton1MouseClicked

