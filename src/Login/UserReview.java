    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(id.getText().isEmpty() || name.getText().isEmpty() || feedback.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,"Please submit a response");
        }
        else{
          try{
            
            Connection con = null;
            ResultSet Rs = null;
            PreparedStatement pst = null;
           
            String userid = LoginSession.Uid;
            String username = name.getText();
            String feeds = feedback.getText();
           
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/poultry", "root", "root");
            PreparedStatement add = con.prepareStatement("insert into feedback values(?,?,?)");
            add.setString(1,userid);
            add.setString(2,username);
            add.setString(3, feeds);
            add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Thank you for the submission ");
            feedback.setText("");
           
            
          }
          catch(Exception e) {
        
          }
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jLabel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel15MouseClicked
        String role = LoginSession.Usertype;
        if(role.equals("Customer"))
        {
            new CHomepage().setVisible(true);
            this.dispose();
        }
        if(role.equals("Farmer"))
        {
            new FHomepage().setVisible(true);
            this.dispose();
        }
        if(role.equals("Merchant"))
        {
            new MHomepage().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jLabel15MouseClicked

