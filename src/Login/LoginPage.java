    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Operations operations = new Operations();
        try{
            
            String useridStr = Uname.getText();
            String passwordStr = Password.getText();
            String usertypeStr = SRole.getSelectedItem().toString(); 
            
            if(operations.isLogin(useridStr ,passwordStr ,usertypeStr,this)) {
                if(Usertype.equals("Customer")){
                    new CHomepage().setVisible(true);
                    this.dispose();
                }
                
                if(Usertype.equals("Farmer")){
                    new FHomepage().setVisible(true);
                    this.dispose();
                }
                
                if(Usertype.equals("Merchant")){
                    new MHomepage().setVisible(true);
                    this.dispose();
                }
                
                if(Usertype.equals("Admin")){
                    new AHomepage().setVisible(true);
                    this.dispose();
                }
                 
                
            }
            else{
              JOptionPane.showMessageDialog(this,"Wrong Userid / Password / Role ");
            }
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Enter correct information");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

